     <p>Vaddio acknowledges that many of our dealers specify customer projects that may go out for bid. The Vaddio Project Registration Request Form is intended to assist authorized Vaddio Resellers who specify these projects by creating demand for our product line.</p>

     <p>By submitting this Project Registration Request Form (and supporting documents detailing your project specification), you are stating that your company has originated the customer project and system design for which you wish to receive special bid pricing.</p>

     <h2>GUIDELINES:</h2>
     <ol>
             <li>Reseller will submit their project registration form at the time of project specification. Vaddio will respond to each registration in writing verifying whether the request is accepted. Submission of the project registration form shall not constitute acceptance by Vaddio and therefore the reseller, until verified by Vaddio management, shall not assume the granting of special pricing.</li>
             
             <li>To be confirmed as the Specifying Reseller of Record, Vaddio must be able to verify that the project is a direct result of the submitting resellers' efforts in creating the original specification/proposal for our products.</li>
             
             <li>If the project registration is not accepted, Vaddio will provide reasons for nonacceptance. Vaddio may from time to time, request additional information to determine the validity of the request. Vaddio maintains the rights to determine the validity of the request and a decision, once rendered by Vaddio, is final.</li>
             
             <li>Upon approval by Vaddio of the project registration form, pricing for that project will be provided to the Specifying Dealer of Record. Any special pricing provided will be valid for that project only, and does not constitute special pricing granted to the reseller for other Vaddio products required for other business.</li>
             
             <li>In order to qualify for Project Registration purposes, the project must require a minimum of $7500.00 (Reseller cost) worth of Vaddio products. Projects smaller than the minimum amount will not be considered for special bid pricing.</li>
             
             <li>This program is only eligible for authorized North American Vaddio resellers in good standing with the company. First time orders with Vaddio from new resellers are not eligible for the program.</li>
             
             <li>Vaddio projects that are originated and specified by design consultants or independent design/build firms are not eligible for project registration by resellers.</li>
             
             <li>Approved project registrations will include expiration dates of the validity of any special pricing granted for the project.</li>
     </ol>

